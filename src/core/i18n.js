export const getTranslation = (lang, key, params = {}) => {
  let text = translations[lang]?.[key] || translations['es']?.[key] || key;
  Object.entries(params).forEach(([k, v]) => {
    text = text.replace(`{${k}}`, v);
  });
  return text;
};
